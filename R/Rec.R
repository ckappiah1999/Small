#' Area of rectangle
#'
#' @param x The length of the rectangle
#' @param y The breath of the rectangle
#'
#' @returns The output from \code{\link{print}}
#' @export
#'
#' @examples
#' arearec (5,8)
arearec <- function(x, y){
  print(paste("The area of a rectangle with length", x,"cm",
              "and breath", y,"cm", "is", x * y,"cm2" ))
}





